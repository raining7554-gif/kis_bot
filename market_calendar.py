def is_trading_day(dt=None) -> bool:
    """정규장 영업일 여부. 인자는 KST aware datetime 권장."""
    if dt is None:
        dt = datetime.now(pytz.timezone("Asia/Seoul"))
    if dt.weekday() >= 5:
        return False
    if dt.strftime("%Y-%m-%d") in KRX_HOLIDAYS:
        return False
    return True
